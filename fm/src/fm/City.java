package fm;

public class City {
	public String name;
	public String country;
	public int population;
	public String photo;
	public City() {
		super();
	}
	public City(String name, String country, int population, String photo) {
		super();
		this.name = name;
		this.country = country;
		this.population = population;
		this.photo = photo;
	}
	
}
