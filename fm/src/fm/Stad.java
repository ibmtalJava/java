package fm;

public class Stad {
	public String name;
	public int capacity;
	public String photo1;
	public City city;
	public Stad() {
		super();
	}
	public Stad(String name, int capacity, String photo1, City city) {
		super();
		this.name = name;
		this.capacity = capacity;
		this.photo1 = photo1;
		this.city = city;
	}
	
}
